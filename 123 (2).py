#thêm 2 biến chiều dài và chiều rộng
length = int(input("Nhập chiều dài cạnh: "))
width = int(input("Nhập chiểu rộng hình chữ nhật: "))
total = length * width #biến total bằng tích của 2 biến chiều dài và chiều rộng
print("Diện tích hình chữ nhật là:")
print(str(length) + " * " + str(width) + " = " + str(total))
print("Diện tích hình chữ nhật là " + str(total)) 